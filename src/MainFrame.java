        System.out.print(courseID + entry.getSemester() + entry.getStatus() + entry.getStudentID());

        ScheduleQueries.addScheduleEntry(entry);

        rebuildStudentDisplayComboBox();
        rebuildSelectStudentComboBoxes();
        rebuildClassComboBoxes();
        scheduleClasesLabel.setText(fullName + " scheduled for " + courseID + " " + status);


