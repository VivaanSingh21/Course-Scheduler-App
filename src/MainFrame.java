    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        
        String fullName = (String) dropStudentComboBox.getSelectedItem();
        String[] arr = fullName.split(" ", 2);
        String firstName = arr[0];
        String lastName = arr[1];
        dropStudentTextArea.setText("");

        String studentID = StudentQueries.getStudentID(firstName, lastName);
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for (String semester : semesters) {
            dropStudentTextArea.append("For semester "+ semester +"\n");

            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            for (ScheduleEntry e : schedule) {
                String status = e.getStatus();
                String courseCode = e.getCourseCode();

                ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByClass(semester, courseCode);

                if ("S".equals(status)) {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);
                    dropStudentTextArea.append("Student " + fullName + " has been dropped from " + courseCode + "\n");

                    if (!waitlistedStudents.isEmpty()) {
                        ScheduleQueries.updateScheduleEntry(waitlistedStudents.get(0));

                        StudentEntry newStudent = StudentQueries.getStudent(waitlistedStudents.get(0).getStudentID());

                        dropStudentTextArea.append("Student " + newStudent.getFirstName() + newStudent.getLastName() + " has been scheduled to " + courseCode + "\n");
                    }

                } else {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);
                    dropStudentTextArea.append("Student " + fullName + " has been dropped from " + courseCode + "\n");
                }

            }
        }


    }//GEN-LAST:event_dropStudentButtonActionPerformed

    private void dropClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropClassButtonActionPerformed
        // TODO add your handling code here:

        currentSemester = (String) currentSemesterComboBox.getSelectedItem();

        String courseCode = (String) dropClassComboBox.getSelectedItem();
        ClassQueries.dropClass(currentSemester, courseCode);
        dropClassTextArea.append(courseCode + " has been dropped\n");
        ArrayList<StudentEntry> students = MultiTableQueries.getScheduledStudentsByClass(currentSemester, courseCode);

        ArrayList<StudentEntry> waitlisted = MultiTableQueries.getWaitlistedStudentsByClass(currentSemester, courseCode);
        dropClassTextArea.append("Scheduled students dropped from this course \n");
        for (StudentEntry s : students) {
            dropClassTextArea.append(s.getFirstName() + " " + s.getLastName() + "\n");
        }
        ScheduleQueries.dropScheduleByCourse(currentSemester, courseCode);
        dropClassTextArea.append("Waitlisted students dropped from this course \n");
        for (StudentEntry s : waitlisted) {
            dropClassTextArea.append(s.getFirstName() + " " + s.getLastName() + "\n");
        }

        rebuildClassComboBoxes();

    }//GEN-LAST:event_dropClassButtonActionPerformed

